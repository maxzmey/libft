int	ft_atoi(const char *str)
{
	int a;
	int b;
	int res;

	a = 0;
	b = 1;
	res = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t'
		|| *str == '\v' || *str == '\f' || *str == '\r')
	{
		str++;
	}
	if (*(str + a) == '-' || *(str + a) == '+')
		b = (*(str + a++) * -1) + 44;
	str += a;
	while (*str >= '0' && *str <= '9')
		res = res * 10 + *str++ - '0';
	return (res * b);
}
